export const dynamic = 'force-dynamic';

async function getCourses() {
  try {
    return await prisma.course.findMany({
      include: {
        category: true,
        country: true,
        _count: {
          select: { lessons: true }
        }
      },
      orderBy: { updatedAt: 'desc' }
    });
  } catch (error) {
    console.error('Error fetching courses:', error);
    return [];
  }
}

async function getStats() {
  try {
    const [totalCourses, publishedCourses, totalLessons, totalCategories] = await Promise.all([
      prisma.course.count(),
      prisma.course.count({ where: { isPublished: true } }),
      prisma.lesson.count(),
      prisma.category.count(),
    ]);

    return {
      totalCourses,
      publishedCourses,
      totalLessons,
      totalCategories,
    };
  } catch (error) {
    return {
      totalCourses: 0,
      publishedCourses: 0,
      totalLessons: 0,
      totalCategories: 0,
    };
  }
}

